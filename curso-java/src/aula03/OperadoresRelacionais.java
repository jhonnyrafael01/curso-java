package aula03;

public class OperadoresRelacionais {

	public static void main(String[] args) {
		//Operadores relacionais. > >= < <= == !=
		
		boolean comparacao = 30 > 30;
		boolean comparacao01 = 30 < 30;
		boolean comparacao02 = 30 >= 30;
		boolean comparacao03 = 30 <= 30;
		boolean comparacao04 = 30 == 30;
		boolean comparacao05 = 30 != 30;
		
		
		System.out.println(comparacao);
		System.out.println(comparacao01);
		System.out.println(comparacao02);
		System.out.println(comparacao03);
		System.out.println(comparacao04);
		System.out.println(comparacao05);
		
		
		
		
	}
}
